public class _03_if_condition {
    public static void main (String[] args) {
        int i = 6;
        if (i == 5) {
            System.out.println("i is odd"); //true
        }
        System.out.println("i is prime"); //true // false

        int k = 6;
        if (k == 5) {
            System.out.println("i is odd"); //true
            System.out.println("i is prime"); //true
        }

    }
}
